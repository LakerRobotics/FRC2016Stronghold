package org.usfirst.frc5053.FRC2016Stronghold.subsystems;

public class Trajectory {
	
	public double x, y, angle, xSpeed, ySpeed, angleSpeed;

	public Trajectory(double x, double y, double angle, double xSpeed, double ySpeed, double angleSpeed) {
		super();
		this.x = x;
		this.y = y;
		this.angle = angle;
		this.xSpeed = xSpeed;
		this.ySpeed = ySpeed;
		this.angleSpeed = angleSpeed;
	}


}
